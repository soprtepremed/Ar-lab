const http = require('http');
const fs = require('fs');
const path = require('path');

const port = process.argv[2] || 8080;
const baseDir = process.argv[3] || '.';

const mimeTypes = {
    '.html': 'text/html',
    '.js': 'text/javascript',
    '.css': 'text/css',
    '.json': 'application/json',
    '.png': 'image/png',
    '.jpg': 'image/jpg',
    '.gif': 'image/gif',
    '.svg': 'image/svg+xml',
    '.wav': 'audio/wav',
    '.mp4': 'video/mp4',
    '.woff': 'application/font-woff',
    '.ttf': 'application/font-ttf',
    '.eot': 'application/vnd.ms-fontobject',
    '.otf': 'application/font-otf',
    '.wasm': 'application/wasm'
};

http.createServer(function (request, response) {
    let safePath = path.normalize(request.url).replace(/^(\.\.[\/\\])+/, '');
    let filePath = path.join(baseDir, safePath === '/' || safePath === '\\' ? 'index.html' : safePath); // Handle changes to root properly
    // Handle query params by stripping them
    if (filePath.includes('?')) filePath = filePath.split('?')[0];

    const extname = String(path.extname(filePath)).toLowerCase();
    const contentType = mimeTypes[extname] || 'application/octet-stream';

    fs.readFile(filePath, function (error, content) {
        if (error) {
            if (error.code == 'ENOENT') {
                // Try index.html if directory
                if (fs.existsSync(filePath) && fs.lstatSync(filePath).isDirectory()) {
                    const index = path.join(filePath, 'index.html');
                    fs.readFile(index, function (e, c) {
                        if (e) {
                            response.writeHead(404);
                            response.end('404 Not Found');
                        } else {
                            response.writeHead(200, { 'Content-Type': 'text/html' });
                            response.end(c, 'utf-8');
                        }
                    });
                } else {
                    response.writeHead(404);
                    response.end('404 Not Found: ' + filePath);
                }
            }
            else {
                response.writeHead(500);
                response.end('Sorry, check with the site admin for error: ' + error.code + ' ..\n');
            }
        }
        else {
            response.writeHead(200, { 'Content-Type': contentType });
            response.end(content, 'utf-8');
        }
    });
}).listen(port);
console.log(`Server running at http://localhost:${port}/ serving ${baseDir}`);
